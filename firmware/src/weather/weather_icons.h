// --------------------------------------------------------------------------
//  Map WMO weather code → icon bitmap pointer
// --------------------------------------------------------------------------
inline const uint8_t* getWeatherIcon(uint8_t wmoCode) {
    // WMO interpretation codes (subset used by Open-Meteo)
    if (wmoCode <= 1)                           return WEATHER_ICON_CLEAR;
    if (wmoCode <= 3)                           return WEATHER_ICON_CLOUDS;
    if (wmoCode == 45 || wmoCode == 48)         return WEATHER_ICON_MIST;
    if ((wmoCode >= 51 && wmoCode <= 67) ||
        (wmoCode >= 80 && wmoCode <= 82))       return WEATHER_ICON_RAIN;
    if ((wmoCode >= 71 && wmoCode <= 77) ||
        wmoCode == 85 || wmoCode == 86)         return WEATHER_ICON_SNOW;
    if (wmoCode == 95 || wmoCode == 96 ||
        wmoCode == 99)                          return WEATHER_ICON_STORM;
    return WEATHER_ICON_CLOUDS; // fallback
}

// --------------------------------------------------------------------------
//  Map WMO code → short condition string (max 18 chars for bottom bar)
// --------------------------------------------------------------------------
inline const char* getWeatherCondition(uint8_t wmoCode) {
    if (wmoCode == 0)                           return "Clear Sky";
    if (wmoCode == 1)                           return "Mainly Clear";
    if (wmoCode == 2)                           return "Partly Cloudy";
    if (wmoCode == 3)                           return "Overcast";
    if (wmoCode == 45 || wmoCode == 48)         return "Fog";
    if (wmoCode >= 51 && wmoCode <= 55)         return "Drizzle";
    if (wmoCode >= 56 && wmoCode <= 57)         return "Freezing Drizzle";
    if (wmoCode >= 61 && wmoCode <= 65)         return "Rain";
    if (wmoCode >= 66 && wmoCode <= 67)         return "Freezing Rain";
    if (wmoCode >= 71 && wmoCode <= 75)         return "Snow";
    if (wmoCode == 77)                          return "Snow Grains";
    if (wmoCode >= 80 && wmoCode <= 82)         return "Rain Showers";
    if (wmoCode == 85 || wmoCode == 86)         return "Snow Showers";
    if (wmoCode == 95)                          return "Thunderstorm";
    if (wmoCode == 96 || wmoCode == 99)         return "Thunderstorm+Hail";
    return "Unknown";
}

